public class Diretor {
    
    // Atributos
    private int dirCod;
    private String dirNome;

    /**
     * @return the dirCod
     */
    public int getDirCod() {
        return dirCod;
    }

    /**
     * @param dirCod the dirCod to set
     */
    public void setDirCod(int dirCod) {
        this.dirCod = dirCod;
    }

    /**
     * @return the dirNome
     */
    public String getDirNome() {
        return dirNome;
    }

    /**
     * @param dirNome the dirNome to set
     */
    public void setDirNome(String dirNome) {
        this.dirNome = dirNome;
    }
}
